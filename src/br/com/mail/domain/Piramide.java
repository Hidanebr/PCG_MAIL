package br.com.mail.domain;

public class Piramide {

	private String mensagemPiramide;
	private String numeroVersao;
	private Modulos modulos;

	public String getMensagemPiramide() {
		return mensagemPiramide;
	}

	public void setMensagemPiramide(String mensagemPiramide) {
		this.mensagemPiramide = mensagemPiramide;
	}

	public String getNumeroVersao() {
		return numeroVersao;
	}

	public void setNumeroVersao(String numeroVersao) {
		this.numeroVersao = numeroVersao;
	}

	public Modulos getModulos() {
		return modulos;
	}

	public void setModulos(Modulos modulos) {
		this.modulos = modulos;
	}

}
